export function getVendorBySlug(slug: string): Vendor | undefined {
  return VENDORS.find((v) => v.slug === slug);
}

export function getVendorsByCounty(countySlug: string): Vendor[] {
  return VENDORS.filter((v) =>
    v.countiesServed.some((c) => c.toLowerCase() === countySlug.toLowerCase())
  );
}

export function getVendorsByCity(city: string, countySlug?: string): Vendor[] {
  return VENDORS.filter((v) => {
    const cityMatch = v.city.toLowerCase() === city.toLowerCase();
    if (countySlug) {
      const countyMatch = v.countiesServed.some(
        (c) => c.toLowerCase() === countySlug.toLowerCase()
      );
      return cityMatch && countyMatch;
    }
    return cityMatch;
  });
}

export function getUniqueCitiesInCounty(countySlug: string): string[] {
  const vendors = getVendorsByCounty(countySlug);
  const cities = new Set(vendors.map((v) => v.city));
  return Array.from(cities).sort();
}

/** All unique cities across all vendors (for site-wide city filter). */
export function getAllUniqueCities(): string[] {
  const cities = new Set(VENDORS.map((v) => v.city));
  return Array.from(cities).sort();
}
